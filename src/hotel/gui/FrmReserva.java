    /**
     * Limita el calendario como fecha minima la actual
     */
    public void fechaMin() {
        cal.add(Calendar.YEAR, +2000);
        Date min = cal.getTime();
        Date max = new Date();
        jCheckIn.setSelectableDateRange(max, min);
        jCheckOut.setSelectableDateRange(max, min);
    }

    /**
     * Carga todas las lista y la tabla
     */
    public void cargarListas() {
        int capacidad = 0;
        String nombre = "";
        TipoHabitacionBo thbo = new TipoHabitacionBo();
        tipos = thbo.cargarTodo();
        HabitacionBo hbo = new HabitacionBo();
        habitaciones = hbo.cargarHabitaciones();
        AgenciaBo abo = new AgenciaBo();
        agencias = abo.cargarTodo();
        for (Habitacion h : habitaciones) {
            switch (h.getNombre()) {
                case "Habitación Simple":
                    capacidad = 1;
                    nombre = "Simple";
                    break;
                case "Habitación Doble":
                    capacidad = 2;
                    nombre = "Doble";
                    break;
                case "Habitación Triple":
                    capacidad = 3;
                    nombre = "Triple";
                    break;
                case "Habitación Cuádruple":
                    capacidad = 4;
                    nombre = "Cuádruple";
                    break;
            }
            for (TipoHabitacion th : tipos) {
                if (h.getTipoHabitacion() == th.getId()) {
                    modelo.addRow(new Object[]{nombre, th.getNombre(), capacidad, th.getPrecio()});
                }
            }
        }
        cbxAgencia.addItem("");
        for (Agencia a : agencias) {
            cbxAgencia.addItem(a.getNombre());
        }
    }

